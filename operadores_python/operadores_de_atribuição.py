#Sao utilizados para definir o valor inicial ou sobreescrever o valor de uma variável

"""
Atribuição simples 

saldo = 500  <-atribui o valor de 500 a variavel saldo

saldo +=200 <-atribui somando o valor de 200 ao valor anterior da variável
saldo /=5   <-atribui dividindo o valor de 5 ao valor anterior da variável
saldo //=5  <-atribui dividindo e pegando só o resultado inteiro o valor de 5 ao valor anterior da variável
saldo %=5   <-atribui pegando o módulo de saldo por 5 ao valor anterio da variável
saldo**=2   <-atribui elevado o valor de saldo a 2ª potencia ao valor anterio da variável

"""

saldo =500
print(saldo)

saldo =200
print(saldo)

saldo += 10
print(saldo)

saldo -= 10
print(saldo)

saldo /= 3
print(saldo)

saldo //= 2
print(saldo)

saldo *= 5
print(saldo)

saldo **= 2
print(saldo)

saldo %= 4
print(saldo)

